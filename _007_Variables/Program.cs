using System;

/*------------------
 * Name: _007_Variables
 * DESC: 변수 선언의 다양한 방법
------------------- */

namespace _007_Variables
{
    class MainClass
    {
        public static void Main(string[] args)
        {
            int x = 10;
            int y = 200;
            int a = 1000, b = 2000; // 변수 선언부

            Console.WriteLine("x : " + x);
            Console.WriteLine("y : " + y);
            Console.WriteLine("a : {0}", a);
            Console.WriteLine("y : {0}", b);


        }
    }
}
